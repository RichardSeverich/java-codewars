package codewars.com.micky.katas;

/**
 * Class.
 */
public final class StringToNumber {

    /**
    * Constructor.
    */
    private StringToNumber() {
    }

    /**
     * @param str str.
     * @return resultado.
     */
    public static int stringToNumber(final String str) {
        int resultado = Integer.parseInt(str);
        return resultado;
    }
}
