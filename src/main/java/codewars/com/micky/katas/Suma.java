package codewars.com.micky.katas;

/**
 * Class.
 */
public final class Suma {

    /**
    * Constructor.
    */
    private Suma() {
    }

    /**
     * @param a a.
     * @param b b.
     * @return c.
     */
    public static int sum(final byte a, final byte b) {
        int c = (a + b);
        return c;
    }
}
