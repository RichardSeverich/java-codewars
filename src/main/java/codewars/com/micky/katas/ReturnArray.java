package codewars.com.micky.katas;

/**
 * Class.
 */
public final class ReturnArray {

    /**
    * Constructor.
    */
    private ReturnArray() {
    }

    /**
     * @param dato dato.
     * @return result.
     */
    public static int taman(final int[] dato) {
        int result = 0;
        result = dato.length;
        return result;
    }
}
