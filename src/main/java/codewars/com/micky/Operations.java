package codewars.com.micky;

/**
 * Class.
 */
public class Operations {

    /**
     * Constructor.
     */
    public Operations() {
    }

    /**
     * @param a a.
     * @param b b.
     * @return a + b.
     */
    public int suma(int a, int b) {
        return a + b;
    }

    /**
     * @param a a.
     * @param b b.
     * @return a - b.
     */
    public int resta(int a, int b) {
        return a - b;
    }

    /**
     * @param a a.
     * @param b b.
     * @return a * b.
     */
    public int multiplicacion(int a, int b) {
        return a * b;
    }

    /**
     * @param a a.
     * @param b b.
     * @return a / b.
     */
    public int division(int a, int b) {
        return a / b;
    }
}
